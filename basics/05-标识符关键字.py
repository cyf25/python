#标识符:自己定义的名字
#标识符命名规则
#1.标识符由字母、数字、下划线组成
#2.标识符不能以数字开头
#3.标识符不能是关键字
#4.标识符区分大小写
#5.标识符命名要见名知意

#关键字
#关键字是python已经使用的标识符，不能用来作为标识符
#关键字列表
